/// [FabType] is used to make the Fab widget.
enum FabStyle {
  /// [FabStyle.basic], used for surface fab
  basic,

  /// [FabStyle.extended], used for primary fab
  extended,
}

enum FabColor {
  /// [FabType.surface], used for surface fab
  surface,

  /// [FabType.primary], used for primary fab
  primary,

  /// [FabType.secondary], used for secondary fab
  secondary,

  /// [FabType.tertiary], used for tertiary fab
  tertiary,
}
