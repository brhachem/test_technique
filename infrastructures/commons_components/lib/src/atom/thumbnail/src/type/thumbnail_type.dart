/// [ThumbNailShape] is used to shape the thumbnail widget.
enum ThumbNailShape {
  /// Default shape is [ThumbNailShape.circle], used for rounded thumbnails
  circle,

  /// [ThumbNailShape.square], used for square thumbnails
  square,
}

enum ThumbNailType {
  /// [ThumbNailType.imageCircle],
  imageCircle,

  /// [ThumbNailType.imageSquare],
  imageSquare,

  /// [ThumbNailType.imageLive],
  imageLive,

  /// [ThumbNailType.iconSquare],
  iconSquare,

  /// [ThumbNailType.iconWithoutSquare],
  iconWithoutSquare,

  /// [ThumbNailType.lettersCircle],
  lettersCircle,

  /// [ThumbNailType.lettersCircleFilled],
  lettersCircleFilled,
}
